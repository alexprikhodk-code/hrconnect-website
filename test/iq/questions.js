// Convert raw correct count (0-15) to IQ score
window.iqFromCorrect = function (correct) {
  // Normal distribution centered on ~9 correct = IQ 100
  // 0 correct → IQ 75, 15 correct → IQ 140
  const total = 15;
  const ratio = correct / total;
  // Linear-ish mapping: IQ = 70 + ratio * 70 → range 70..140
  return Math.round(70 + ratio * 70);
};

window.iqBand = function (iq) {
  if (iq >= 135) return "Дуже високий";
  if (iq >= 110) return "Високий";
  if (iq >= 100) return "Вище середнього";
  if (iq >= 90)  return "Середній";
  if (iq >= 80)  return "Низький";
  return "Дуже низький";
};

